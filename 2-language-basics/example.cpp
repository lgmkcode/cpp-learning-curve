#include <iostream>
using namespace std;

int main(){
    int a, b;
    cout << "Enter a first number:";
    cin >> a;
    cout << "Enter a second number:";
    cin >> b;

    cout << "a+b=" << a+b << endl;
    cout << "a-b=" << a-b << endl;
    cout << "a*b=" << a*b << endl;
    cout << "a/b=" << (float)a/b << endl;
    cout << "a%b=" << a%b << endl;
 return 0;
}
